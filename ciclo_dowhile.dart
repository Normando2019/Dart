main(List<String> args) {
  //Ciclo Do While
  int i = 0;
  do {
    print('Valor de i = $i');
    i++;
  } while (i < 10);

  print('');
  i = 0;
  do {
    i++;
    print('Valor de i = $i');
  } while (i < 10);

  print('');
  i = 0;
  do {
    print('Valor de i = $i');
    i--;
  } while (i > -10);
}
